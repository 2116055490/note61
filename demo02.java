/*
*
*
* public   公有的，公共的，其他的程序都可以访问，最大的权限
*   class 类
*    Main  类的名称，随便取名字，最好有意义，一般要求首字母大写  */
public class demo02 {
    //属性  定义在类中  有对应的默认值如果基本数据类型的变量定义在方法中，不给值则没有默认值
    public static void main(String[] args) {
        /*
        * static  静态的    没有对象也可以调用 属于公共资源
        * void  空   方法的返回值 由于main是主方法 不需要给任何程序返回内容，因此就写空
        * main  主要的  方法的名字，可以随便取，但是入口主方法只能写成main 自定义的方法名可以自己取，建议有意义，首字母小写 驼峰命名法
        * (String[] args)   方法的参数    String[]数组类型   args数组名字
        * */
    }
}
